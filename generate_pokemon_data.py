def get_pinyin_for_char(char: str) -> str:
    """è·å–å•ä¸ªæ±‰å­—çš„æ‹¼éŸ³ï¼ˆå¸¦å£°è°ƒï¼‰"""
    result = pinyin(char, style=Style.TONE, heteronym=False)
    return result[0][0] if result else char


def split_pinyin(py: str) -> tuple:
    """
    æ‹†åˆ†æ‹¼éŸ³ä¸ºå£°æ¯å’ŒéŸµæ¯
    è¿”å›: (å£°æ¯, éŸµæ¯å«å£°è°ƒ)
    """
    # å£°æ¯åˆ—è¡¨
    initials = ['zh', 'ch', 'sh', 'b', 'p', 'm', 'f', 'd', 't', 'n', 'l',
                'g', 'k', 'h', 'j', 'q', 'x', 'r', 'z', 'c', 's', 'y', 'w']

    # ç§»é™¤å£°è°ƒå¾—åˆ°çº¯å­—æ¯
    py_no_tone = ''.join([c for c in py if c.isalpha()])

    # æŸ¥æ‰¾å£°æ¯
    initial = ''
    for ini in initials:
        if py_no_tone.startswith(ini):
            initial = ini
            break

    # éŸµæ¯ = å®Œæ•´æ‹¼éŸ³ - å£°æ¯
    if initial:
        final = py[len(initial):]
    else:
        final = py

    return initial, final


def generate_challenges(name: str, pinyins: List[str]) -> List[Dict]:
    """ä¸ºæ¯ä¸ªå­—ç”Ÿæˆæ‹¼éŸ³æŒ‘æˆ˜é¢˜ç›®"""
    challenges = []

    for idx, (char, py) in enumerate(zip(name, pinyins)):
        initial, final = split_pinyin(py)

        # ç”Ÿæˆå£°æ¯é¢˜ç›®
        if initial:
            # è·å–æ˜“æ··æ·†çš„å£°æ¯ä½œä¸ºå¹²æ‰°é¡¹
            wrong_options = CONFUSING_INITIALS.get(initial, [])
            if len(wrong_options) < 2:
                # å¦‚æœæ²¡æœ‰é¢„è®¾çš„æ··æ·†é¡¹ï¼Œéšæœºé€‰æ‹©
                all_initials = list(CONFUSING_INITIALS.keys())
                wrong_options = random.sample([i for i in all_initials if i != initial], 2)
            else:
                wrong_options = wrong_options[:2]

            options = [initial] + wrong_options
            random.shuffle(options)

            challenges.append({
                "target": initial,
                "type": "initial",
                "options": options,
                "charIndex": idx
            })

        # ç”ŸæˆéŸµæ¯é¢˜ç›®ï¼ˆå¸¦å£°è°ƒï¼‰
        if final and len(final) > 0:
            # è·å–ä¸å¸¦å£°è°ƒçš„éŸµæ¯
            final_no_tone = ''.join([c for c in final if c.isalpha()])

            # ç”Ÿæˆé”™è¯¯çš„å£°è°ƒé€‰é¡¹
            tones = ['Ä', 'Ã¡', 'Ç', 'Ã '] if 'a' in final else \
                    ['Ä“', 'Ã©', 'Ä›', 'Ã¨'] if 'e' in final else \
                    ['Ä«', 'Ã­', 'Ç', 'Ã¬'] if 'i' in final else \
                    ['Å', 'Ã³', 'Ç’', 'Ã²'] if 'o' in final else \
                    ['Å«', 'Ãº', 'Ç”', 'Ã¹'] if 'u' in final else [final]

            # è·å–æ˜“æ··æ·†çš„éŸµæ¯
            wrong_finals = CONFUSING_FINALS.get(final_no_tone, [])
            if len(wrong_finals) < 2:
                # éšæœºé€‰æ‹©å…¶ä»–éŸµæ¯
                all_finals = list(CONFUSING_FINALS.keys())
                wrong_finals = random.sample([f for f in all_finals if f != final_no_tone], 2)

            # æ„å»ºé€‰é¡¹
            options = [final]
            for wf in wrong_finals[:2]:
                # æ·»åŠ éšæœºå£°è°ƒ
                if any(c in wf for c in 'aeiou'):
                    options.append(wf + random.choice(['', 'Ì„', 'Ì', 'ÌŒ', 'Ì€']))
                else:
                    options.append(wf)

            random.shuffle(options)

            challenges.append({
                "target": final,
                "type": "final",
                "options": options[:3],
                "charIndex": idx
            })

    return challenges


